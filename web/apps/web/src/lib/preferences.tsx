export type TranslationKey = keyof typeof ZH

type TranslateVars = Record<string, string | number | null | undefined>

interface PreferencesContextValue {
  locale: Locale
  theme: ThemeMode
  setLocale: (locale: Locale) => void
  setTheme: (theme: ThemeMode) => void
  toggleTheme: () => void
  t: (key: TranslationKey, vars?: TranslateVars) => string
}

const PreferencesContext = createContext<PreferencesContextValue | null>(null)

export function PreferencesProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => readLocale())
  const [theme, setThemeState] = useState<ThemeMode>(() => readTheme())

  useEffect(() => {
    document.documentElement.lang = locale
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
  }, [locale])

  useEffect(() => {
    document.documentElement.classList.toggle('dark', theme === 'dark')
    document.documentElement.style.colorScheme = theme
    window.localStorage.setItem(THEME_STORAGE_KEY, theme)
  }, [theme])

  const setLocale = useCallback((nextLocale: Locale) => setLocaleState(nextLocale), [])
  const setTheme = useCallback((nextTheme: ThemeMode) => setThemeState(nextTheme), [])
  const toggleTheme = useCallback(() => setThemeState((c) => c === 'dark' ? 'light' : 'dark'), [])
  const t = useCallback((key: TranslationKey, vars?: TranslateVars): string => translate(locale, key, vars), [locale])

  const value = useMemo(() => ({ locale, theme, setLocale, setTheme, toggleTheme, t }), [locale, theme, setLocale, setTheme, toggleTheme, t])

  return (
    <PreferencesContext.Provider value={value}>
      {children}
    </PreferencesContext.Provider>
  )
}

export function usePreferences(): PreferencesContextValue {
  const context = useContext(PreferencesContext)
  if (!context) {
    throw new Error('usePreferences must be used inside PreferencesProvider')
  }
  return context
}

function translate(locale: Locale, key: TranslationKey, vars?: TranslateVars): string {
  const template = (locale === 'en-US' ? EN : ZH)[key] || ZH[key]
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (match, name) => {
    const value = vars[name]
    return value == null ? match : String(value)
  })
}

function readLocale(): Locale {
  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  return saved === 'en-US' || saved === 'zh-CN' ? saved : 'zh-CN'
}

function readTheme(): ThemeMode {
  const saved = window.localStorage.getItem(THEME_STORAGE_KEY)
  if (saved === 'light' || saved === 'dark') return saved
  return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light'
}
